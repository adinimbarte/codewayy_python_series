# declaring tuple
tuple = (5, 1, 9, 6, 9, 8, 52)


# Code for printing the length of a tuple
tuple2 = ('python', 'c', 'java')
print('length of tuple2 :', len(tuple2))

# method to delete the tuple
del tuple2

# print tuple till 2 index excluding 2
print('tuple[0:2]:', tuple[0:2])

# display the tuple in reverse order
print('tuple[::-1]:', tuple[::-1])

# print element with maximum value from tuple
print('maximum element in tuple:', max(tuple))

# print element with maximum value from tuple
print('minimum element in tuple:', min(tuple))

# prints number of times a specified element occurs in a tuple
print('Number of times 9 repeated:', tuple.count("9"))

# displays the index of specified element
print('index value of element "6":', tuple.index(6))


